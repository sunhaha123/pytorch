import torch

t = torch.randn(2,3,4) # 构造2×3×4的张量
t
t[1,2,3] # 取张量在0维1号、1维2号、2维3号的元素（编号从0开始）
t[:,1:-1,1:3] # 仅仅一个冒号表示取所有的，-1表示最后一个元素
t[1,2,3] = -10
t # 直接更改索引和切片会更改原始张量的值
t > 0 # 张量大于零部分的掩码
t[t>0] # 根据掩码选择张量的元素，注意最后选出来的是一个向量